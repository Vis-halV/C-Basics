#include<iostream>
using namespace std;

int main() {
    int x = 10;
    int y = 5;

    // Addition

    int sum = x + y; 
    cout<<"The sum of "<< x << " and " << y << " is : " << sum <<endl;

    // Subtraction

    int diff = x - y; 
    cout<<"The difference of "<< x << " and " << y << " is : " << diff <<endl;
    
    // Multipllication

    int mul = x * y; 
    cout<<"The sum of "<< x << " and " << y << " is : " << mul <<endl;

    // Division

    int div = x / y; 
    cout<<"The divisor of "<< x << " and " << y << " is : " << div <<endl;

    // Remainder

    int r = x % y; 
    cout<<"The sum of "<< x << " and " << y << " is : " << r <<endl;

    return 0;

}